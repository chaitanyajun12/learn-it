// typeof operator is used to find the type of the value.

var name = "Krishna";
console.log(typeof name); // string type

var num = 100;
console.log(typeof num); // number type

var x = true;
console.log(typeof x); // boolean type

var y = null;
console.log(typeof y); //object type :O

var z = undefined;
console.log(typeof z); // undefined type

var arr = ['hello', 'world'];
console.log(typeof arr); // object type

var obj = { "name": "Krishna Chaitanya M" };
console.log(typeof obj); // object type

var func = function hello() {};
console.log(typeof func); // function type