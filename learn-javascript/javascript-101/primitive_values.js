"use strict";

// Values are fundamental unit if information in a program.
// They come in two types, primitives and object.
// Values are embedded in code using literals.
// Literal is a notation used to represent fixed values.

// string literals
var name = 'Krishna Chaitanya M';
console.log(name);

// integer literals
var number = 100;
console.log(number);

// boolean primitive value
var isLiteral = true;
console.log(isLiteral);

// null primitive value
var x = null;
console.log(x);

// undefined primitive value
var y = undefined;
if (y === undefined) {
    console.log(y);
}